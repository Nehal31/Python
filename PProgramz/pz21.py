''' take a decimal number print its binary, octal, hexadecimal '''

#take the input
n = int(input('Enter the Decimal number : '))

print('Decmal Number {0}'.format(n))
print('Binary Number {0}'.format(bin(n)))
print('Octal Number {0}'.format(oct(n)))
print('HexaDecimal Number {0}'.format(hex(n)))




