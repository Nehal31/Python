''' check the given number is +ve, -ve or 0 '''

#taking the given number
n = float(input('Enter the number : '))

#check the numner
#check for positive number
if n > 0 :
    print('Positive Number')
    
#check for negative number
elif n < 0:
    print('Negative Number')

#else number is zero
else:
    print('Zero')




